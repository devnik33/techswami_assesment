/**
 * Problem 1: Module and exports
 *
 * Implement a function `add` that returns the sum of two numbers.
 * Implement a function `multiply` that returns the product of two numbers.
 * Export both functions.
 *
 * @returns {object} - The calculator object.
 */
exports.add=(a,b)=>{
    return a+b;
}

exports.multiply=(a,b)=>{
    return a*b;
}
